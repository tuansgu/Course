    public void loadCourseIdInLabel() {
        String title = "";
        String courseType = "";
        title = course.getTitleById(Integer.parseInt(CourseID));
        courseType = course.getCourseTypeById(Integer.parseInt(PersonID));
        jLabel5.setText(CourseID + " - " + title + " - " + courseType);
    }
    
    public void loadPersonIdInCombobox() {
        int[] personId = new int[0];
        personId = courseInstructor.getAllPersonId();
       
        jComboBox1.removeAllItems();

        for (int item : personId) {
            Person = courseInstructor.getNamePersonById(item);
            jComboBox1.addItem(String.valueOf(item) + " - " + Person.getFirstName() + " " + Person.getLastName());
        }
        Person = courseInstructor.getNamePersonById(Integer.parseInt(PersonID));
        jComboBox1.setSelectedItem(PersonID + " - " + Person.getFirstName() + " " + Person.getLastName());
    }

