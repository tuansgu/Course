    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            int value1 = Integer.parseInt(CourseID);

            String stringValue2 = (String) jComboBox1.getSelectedItem();
            String[] parts = stringValue2.split(" - ");
            String PersonId = parts[0];
            int value2 = Integer.parseInt(PersonId);
            
            courseInstructorDto = new CourseInstructorDTO();

            courseInstructorDto.setCourseID(value1);
            courseInstructorDto.setPersonID(value2);

            int rs = courseInstructor.updateCourseInstructor(courseInstructorDto);
            if(rs == 1) {
                this.dispose();
                JOptionPane.showMessageDialog(null, "Update successful!", "Success", JOptionPane.INFORMATION_MESSAGE);
                homeGui.loadCourseInstructor();
                
            } else {
                JOptionPane.showMessageDialog(null, "Update failed!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid input! Please select valid integers.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

