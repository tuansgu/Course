    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String studentname = getStringAfterSpace(jComboBoxStudentID.getSelectedItem().toString());
        String title = getStringAfterDash(jComboBoxCourseID.getSelectedItem().toString());

        int courseID = StudentGradeBLL.getCourseID(title).getCourseID();
        int studentID = StudentGradeBLL.getStudentID(studentname).getPersonID();

        boolean check = StudentGradeBLL.addNewStudentGrade(courseID, studentID);
        if (check) {
            JOptionPane.showMessageDialog(this, "Thêm thành công");
        } else {
            JOptionPane.showMessageDialog(this, "Sinh viên đã tham gia khóa học");
        }
        

    }//GEN-LAST:event_jButton1ActionPerformed

    public String getStringAfterDash(String input) {
        int index = input.indexOf("-");
        if (index != -1) {
            return input.substring(index + 1).trim();
        }
        return input;
    }

    public String getStringAfterSpace(String input) {
        int index = input.indexOf(" ");
        if (index != -1) {
            return input.substring(index + 1).trim();
        }
        return input;
    }

