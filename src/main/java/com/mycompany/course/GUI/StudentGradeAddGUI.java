    public void loadComboboxCourseID() {
        try {
            ArrayList<CourseDTO> arr = StudentGradeBLL.getAllCourse();
            for (int i = 0; i < arr.size(); i++) {
                jComboBoxCourseID.addItem(arr.get(i).getCourseID() + " - " + arr.get(i).getTitle());
            }
        } catch (SQLException ex) {
            Logger.getLogger(HomeGUI.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void loadComboboxStudentID() {
        try {
            ArrayList<PersonDTO> arr = StudentGradeBLL.getAllStudent();
            for (int i = 0; i < arr.size(); i++) {
                jComboBoxStudentID.addItem(arr.get(i).getLastName() + " " + arr.get(i).getFirstName());
            }
        } catch (SQLException ex) {
            Logger.getLogger(HomeGUI.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

