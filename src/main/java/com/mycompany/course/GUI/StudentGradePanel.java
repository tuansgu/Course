    private void btnSearchStudentsNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchStudentsNameActionPerformed
        try {
            if (txtfindStudentsName.getText().isBlank() == false) {

                String textSearch = jComboBox1.getSelectedItem().toString();
                if (textSearch == "EnrollmentID") {
                    searchEnrollmentID();
                } else if (textSearch == "StudentID") {
                    searchStudentID();
                } else if (textSearch == "Name") {
                    StudentGradeBLL.findStudentName(txtfindStudentsName.getText());
                }
            } else {
                JOptionPane.showMessageDialog(this, "Nhập từ kháo càn tìn", "Message", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException ex) {
            Logger.getLogger(HomeGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSearchStudentsNameActionPerformed

