    public void search(String txtQuery, String txtSearch) throws SQLException {
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        while (jTable1.getRowCount() > 0) {
            tableModel.removeRow(0);
        }
        List<StudentGradeDTO> list = StudentGradeBLL.findStudentGrade(txtQuery, txtSearch);
        for (int i = 0; i < list.size(); i++) {
            String title = StudentGradeBLL.getTitle(list.get(i).getCourseID()).getTitle();
            String lastName = StudentGradeBLL.getName(list.get(i).getStudentID()).getLastName();
            String firstName = StudentGradeBLL.getName(list.get(i).getStudentID()).getFirstName();

            tableModel.addRow(new Object[]{list.get(i).getEnrollmentID(),
                list.get(i).getCourseID(), title,
                list.get(i).getStudentID(),
                lastName + " " + firstName,
                list.get(i).getGrade()}
            );
        }
    }

    public void searchEnrollmentID() throws SQLException {
        String EnrollmentID = txtfindStudentsName.getText();
        String query = "EnrollmentID";
        search(query, EnrollmentID);
    }

    public void searchStudentID() throws SQLException {
        String StudentID = txtfindStudentsName.getText();
        String query = "StudentID";
        search(query, StudentID);
    }

